# Crie uma funçao que soma dois paramentros e retorne o resultado.

# def é a função
# soma é o nome da função
# numero1 e numero2 são os parâmetros da função



def soma(numero1, numero2):
    return numero1 + numero2
num = int(input('Digite um número: '))
num2 = int(input('Digite outro número: '))      
print(f'A soma de {num} + {num2} é: {soma(num, num2)}')


