#Ultilizando Break 


i = 1
while i < 10:
  print(i)
  if (i == 5):
    break         # O loop é interrompido quando i == 5  
  i += 1     



#Ultilizando continue

i = 0
while i < 10:
  i += 1
  if i == 5:
    continue # O loop é interrompido quando i == 3
  print(i)  # O número 3 não será impresso
  
  
  
  
